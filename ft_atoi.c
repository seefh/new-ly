int		ft_atoi(const char *c)
{
	int	nbr;
	int i;
	int sign;

	nbr = 0;
	i = 0;
	sign = 1;
	if (!c[i])
		return (0);
	while (c[i] == '\n' || c[i] == '\r' || c[i] == '\v'
			|| c[i] == '\f' || c[i] == '\t' || c[i] == ' ')
		i++;
	if (c[i] == '-' || c[i] == '+')
	{
		if (c[i] == '-')
			sign = -1;
		i++;
	}
	while (c[i] >= '0' && c[i] <= '9')
	{
		nbr = (nbr * 10) + (c[i] - '0');
		i++;
	}
	return (nbr * sign);
}
