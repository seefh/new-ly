#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int		i;
	char	*c;

	c = (char*)s;
	i = 0;
	while (c[i] != '\0')
	{
		ft_putchar_fd(c[i], fd);
		i++;
	}
}
