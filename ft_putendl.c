#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	*c;

	c = (char*)s;
	i = 0;
	while (c[i] != '\0')
	{
		ft_putchar(c[i]);
		i++;
	}
	ft_putchar('\n');
}
